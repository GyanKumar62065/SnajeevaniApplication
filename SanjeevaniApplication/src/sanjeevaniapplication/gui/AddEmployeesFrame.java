    private void btnAddEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddEmpActionPerformed
        
        boolean isValidDetails = isValidInput();
        if(!isValidDetails){
            JOptionPane.showMessageDialog(null , "Please Input Valid Employee Details!");
            return;
        }
        
        String selectedJob = getselectedJob().toLowerCase();
        System.out.println(selectedJob); // For Debugging
        try{
            double eSal = Double.parseDouble(empSal);
            EmpPojo empPojo = new EmpPojo(newEmpId , empName , selectedJob , eSal);
            boolean bool = EmpDao.addEmployees(empPojo);
            if(bool){
                JOptionPane.showMessageDialog(null, "Employee added successfully!" , "Success!" , JOptionPane.INFORMATION_MESSAGE);
                getNewId();
                clearInputs();
                return;
            }
            JOptionPane.showMessageDialog(null, "Something wrong please try again!", "Error!", JOptionPane.ERROR_MESSAGE);
            return;
            
        }
        catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Please Input numeric salary!", "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();

        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in Db:" + ex.getMessage(), "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
               
    }//GEN-LAST:event_btnAddEmpActionPerformed

