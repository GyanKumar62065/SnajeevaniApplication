    private void getNewId() {
        try{
            newEmpId = EmpDao.getNewEmpId();
            txtEmpId.setText(newEmpId);
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB:- " + ex.getMessage());
            ex.printStackTrace();
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Number Format Exception In new Emp Id Generation");
            ex.printStackTrace();
        }
        
    }

    private boolean isValidInput() {
        empName = txtEmpName.getText().trim();
        empSal = txtEmpSal.getText().trim();
        if(empName.isEmpty()){
            return false;
        }else if(empSal.isEmpty()){
            return false;
        }
        return true;
    }

    private String getselectedJob() {
        return jcbJob.getSelectedItem().toString();
    }

    private void clearInputs() {
        txtEmpName.setText("");
        txtEmpSal.setText("");
        jcbJob.setSelectedIndex(0);
    }
