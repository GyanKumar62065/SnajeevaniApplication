    private void getAllEmployeesId() {
        try{
            List<String>empId = EmpDao.getAllEmpId();
            if(empId.isEmpty()){
                JOptionPane.showMessageDialog(null, "Don't have any employee Id!");
                return;
            }
            for(String s : empId){
                jcbEmpId.addItem(s);
            }
        }catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB:" + ex.getMessage(), "Error!", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    private boolean isValidData() {
        empId = jcbEmpId.getSelectedItem().toString();
        empSal = txtEmpSal.getText().trim();
        empName = txtEmpName.getText().trim();
        empDepartment = jcbEmpJob.getSelectedItem().toString();
        if(empName.isEmpty()) return false;
        else if(empSal.isEmpty()) return false;
        return true;
    }
