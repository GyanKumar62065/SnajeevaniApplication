    private void btnUpdateEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateEmpActionPerformed
        
        boolean isValidInput = isValidData();
        if(!isValidInput){
            JOptionPane.showMessageDialog(null, "Please enter valid input!");
            return;
        }
        try{
            double eSal = Double.parseDouble(empSal);
            EmpPojo emp = new EmpPojo(empId , empName , empDepartment.toLowerCase(), eSal);
            boolean bool = EmpDao.updateEmployees(emp);
            if(bool){
              JOptionPane.showMessageDialog(null,"Record updated successfully!","Success",JOptionPane.INFORMATION_MESSAGE);
              if(UserProfile.getUserId().equalsIgnoreCase(emp.getEmpId()))
                  UserProfile.setUserName(empName);
              return;
              
            }
          JOptionPane.showMessageDialog(null,"Record not updated!","Failure!",JOptionPane.ERROR_MESSAGE);    
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null, "Error in DB: " + ex.getMessage(), "ERROR!" , JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
            
        }catch(NumberFormatException ex){
            JOptionPane.showMessageDialog(null, "Please enter numeric salary", "ERROR!" , JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_btnUpdateEmpActionPerformed

    private void jcbEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcbEmpIdActionPerformed
        try{
            String empId = jcbEmpId.getSelectedItem().toString();
            EmpPojo emp = EmpDao.getEmpDetailsByEmpId(empId);
            txtEmpName.setText(emp.getEmpName());
            txtEmpSal.setText(String.valueOf(emp.getEmpSal()));
            jcbEmpJob.setSelectedItem(emp.getEmpDepartment().toUpperCase());
        }
        catch(SQLException ex){
            JOptionPane.showMessageDialog(null,"Error In DB"+ex.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        
    }//GEN-LAST:event_jcbEmpIdActionPerformed

