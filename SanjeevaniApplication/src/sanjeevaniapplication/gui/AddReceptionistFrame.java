    private void btnRegisterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegisterActionPerformed
        if(validateInputs()==false){
             JOptionPane.showMessageDialog(null, "Please input all the fields");
             return;
        }
        String passwordValid=matchPassword();
        if(passwordValid!=null){
            JOptionPane.showMessageDialog(null, passwordValid);
            return;
        }

        try{
            String encryptedPassword = new String(PasswordEncryption.getEncryptedPassword(password));
            UsersPojo2 user = new UsersPojo2(loginId,recpName,encryptedPassword , "RECEPTIONIST");
            boolean res1 = UsersDao.addUser(user);
            ReceptionistPojo recp = new ReceptionistPojo(recpId, recpName, jcbGender.getSelectedItem().toString());
            boolean res2 = ReceptionistDao.addReceptionist(recp);
            
            if(res1==true && res2==true){
             JOptionPane.showMessageDialog(null, "Receptionist successfully registered!");
             clearAll();
             generateReceptionistId();
             return;
            }
            JOptionPane.showMessageDialog(null, "Registration unsuccessful!"); 
            return; 
        }catch(SQLException e){
            e.printStackTrace();
        }
        
    }//GEN-LAST:event_btnRegisterActionPerformed

