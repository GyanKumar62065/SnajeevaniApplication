    private void getAllUnregisterReceptionist() {
        try{
            unRegisterReceptionist = EmpDao.getUnRegisteredReceptionist();
            Set<String>recIdSet = unRegisterReceptionist.keySet();
            for(String s : recIdSet){
                jcbEmpId.addItem(s);
            }
        }catch(SQLException e){
            e.printStackTrace();
        }
    }

    private void generateReceptionistId() {
        try{
            recpId = ReceptionistDao.getNewReceptionistId();
            txtReceptionistId.setText(recpId);
        }catch(SQLException e){
            e.printStackTrace();
        }
    }
    
    private boolean validateInputs(){
      this.loginId=this.txtLoginId.getText().trim();
      char[] p1=txtPwd.getPassword();
      char[] p2=txtCnfPwd.getPassword();
      if(loginId.isEmpty()||p1.length==0||p2.length==0)
          return false;
      password=new String(p1);
      cnfPassword=new String(p2);
      return true;
      
  }
  private String matchPassword(){
     password=password.trim();
     cnfPassword=cnfPassword.trim();
     if(password.length()<3 || cnfPassword.length()<3)
         return "Passwords must be of atleast 3 characters in length";
     if(password.equals(cnfPassword)==false)
         return "Passwords do not match";
     return null;
  }

    private void clearAll() {
        txtLoginId.setText("");
        txtPwd.setText("");
        txtCnfPwd.setText("");
        empId = jcbEmpId.getSelectedItem().toString();
        this.unRegisterReceptionist.remove(empId);
        this.jcbEmpId.removeItem(empId);
        if(unRegisterReceptionist.size()==0){
            JOptionPane.showMessageDialog(null,"All Receptionists registered!");
            ManageReceptionistsFrame manageRecp=new ManageReceptionistsFrame();
            manageRecp.setVisible(true);
            this.dispose();
        }
    }

